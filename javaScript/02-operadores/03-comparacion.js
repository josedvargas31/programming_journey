let a = 10;

// Operadores relacionales
console.log(a > 5); // evaluamos si la variable a es mayor a cinco
console.log(a >= 5); // evaluamos si la variable a es mayor o igual a cinco
console.log(a < 5); // evaluamos si la variable a es menor a cinco
console.log(a <= 5); // evaluamos si la variable a es nemor o igual a cinco

// operadores de igualdad

/* No se utilizan mucho estos '==' o '!=' */
console.log(a == 10); // evaluamos si la variable a es exactamente igual a 10
console.log(a != 10); // evaluamos si la variable a es distanta a 10
console.log(a == '10'); // evalua si la variable es igual a diez, es decir el valor, no importa que tipo de dato sea

/* Recomendados usar */
console.log(a === '10'); // evalua si la variable es igual a diez, tambien el tipo de dato
console.log(a !== '110'); // evalua si la variable es a, es diferente a 10 o  tambien el tipo de dato


