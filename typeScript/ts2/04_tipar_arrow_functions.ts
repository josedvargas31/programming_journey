
// tipamos los parametros aqui: '(a: number, b: number)', dos puntos y el tipo que retorna 
const sumar = (a: number, b: number): number => {
    return a + b;
  };
  
// tipamos y retornamos el tipo de dato aqui '(a: number, b: number) => number' 
// los parametros de la funcion '(a ,b)'
const restar: (a: number, b: number) => number = (a ,b) => {
    return a - b;
};